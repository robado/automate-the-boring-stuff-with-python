# Comparison Operators
# == Equal to
# != Not equal to
# < Less than
# > Greater than
# <= Less than or equal to
# >= Greater than or equal to
print(42 == 42)  # True
print(42 == 'Hello')  # False
print(42 != 3)  # True
print(42 >= 100)  # False
print(42 < 42)  # False
print(42 <= 42)  # True
myAge = 26
print(myAge == 26)  # True
print(myAge < 30)  # True
